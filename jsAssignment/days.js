let days = prompt("Choose a number to display a day: 1, 2, 3, 4, 5, 6, 7");
switch (days){
    case '1':
    alert("Monday");
    break;
    case '2':
    alert("Tuesday");
     break;
    case '3':
    alert("Wednesday");
    break;
    case '4':
    alert("Thursday");
    break;
    case '5':
    alert("Friday");
    break;
    case '6':
    alert("Saturday");
    break;
    case '7':
    alert("Sunday");
    default:
    alert("No such day");
}