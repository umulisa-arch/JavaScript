//conditionals
let temperature = 70;
console.log(`The temperature is: ${temperature}`);
 
if (temperature > 70) {
   console.log("It's warm outside");
}
else if(temperature<70){
    console.log('it is cold outside!');
}
else{
    console.log("it is an average temperature");
}

